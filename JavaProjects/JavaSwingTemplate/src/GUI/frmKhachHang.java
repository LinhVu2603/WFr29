    public boolean kiemTraEmail(String email) {
        String dinhDangEmail = "^[_A-Za-z0-9-]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
        boolean ktEmail = email.matches(dinhDangEmail);
        return ktEmail;
    }
    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        String sdt = txt_Sdt.getText();
        if (sdt.length() == 0) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không được trống!");
        } else {
            if ((sdt.length() != 10) && (sdt.charAt(0) != 0)) {
                JOptionPane.showMessageDialog(this, "Số điện thoại sai định dạng!");
                return;
            }
            String email = txt_Email.getText();
            if (!kiemTraEmail(email)) {
                JOptionPane.showMessageDialog(this, "Email sai định dạng!");
                return;
            }
            String hoTen = txt_ten.getText();
            if (hoTen.equals("")) {
                JOptionPane.showMessageDialog(this, "Họ tên không được để trống!");
                return;
            }
            String matKhau = txt_matKhau.getText();
            String gioiTinh = "Nam";
            if (txt_gioiTinh.getSelectedItem().equals("Nữ")) {
                gioiTinh = "Nữ";
            } else if (txt_gioiTinh.getSelectedItem().equals("Khác")) {
                gioiTinh = "Khác";
            }
            int trangThai = 0;
            if (txt_trangThai.getSelectedItem().equals("Active")) {
                trangThai = 1;
            }
            int nhom = 0;
            if (txt_nhom.getSelectedItem().equals("Quản lý")) {
                nhom = 1;
            }
            //Cập nhật khách hàng mới
            kh.setHoTen(hoTen);
            kh.setEmail(email);
            kh.setSoDienThoai(sdt);
            kh.setGioiTinh(gioiTinh);
            kh.setTrangThai(trangThai);
            kh.setNhom(nhom);
            //Set Arraylist
            listKH.set(index, kh);
            //Lưu file
            saveFile();
            //Cập nhật table
            getData(listKH);
            JOptionPane.showMessageDialog(this, "Chỉnh sửa khách hàng " + kh.getMaKhachHang() + " thành công!");
            //Reset form
            reset();
        }
    }//GEN-LAST:event_btn_suaActionPerformed

    private void btn_timKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timKiemActionPerformed
        String tuKhoa = txt_timKiem.getText();
        if (tuKhoa.equals("")) {
            getData(listKH);
        } else {
            ArrayList<KhachHang> listKH_Search = new ArrayList<>();
            for (int i = 0; i < listKH.size(); i++) {
                if ((listKH.get(i).getHoTen().contains(tuKhoa)) || (listKH.get(i).getEmail().contains(tuKhoa)) || (listKH.get(i).getMaKhachHang().contains(tuKhoa))) {
                    listKH_Search.add(listKH.get(i));
                }
            }

            getData(listKH_Search);
            if (listKH_Search.size() == 0) {
                JOptionPane.showMessageDialog(this, "Khách hàng cần tìm không tồn tại!");
                reset();
                getData(listKH);
            }
        }
    }//GEN-LAST:event_btn_timKiemActionPerformed
