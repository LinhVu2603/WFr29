    private void tbl_khachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_khachHangMouseClicked
        btn_sua.setEnabled(true);
        btn_xoa.setEnabled(true);
        //Lay ra ban ghi
        int row = tbl_khachHang.getSelectedRow();
        String maKH = tbl_khachHang.getValueAt(row, 0).toString();
        for (int i = 0; i < listKH.size(); i++) {
            if (maKH.equals(listKH.get(i).getMaKhachHang())) {
                kh = listKH.get(i);
                index = i;
                break;
            }
        }

        txt_MaKH.setEnabled(false);
        txt_matKhau.setEnabled(false);
        btn_them.setEnabled(false);

        txt_MaKH.setText(kh.getMaKhachHang());
        txt_Email.setText(kh.getEmail());
        txt_Sdt.setText(kh.getSoDienThoai());
        txt_matKhau.setText(kh.getMatKhau());
        txt_ten.setText(kh.getHoTen());

        if (kh.getGioiTinh().equals("Nam")) {
            txt_gioiTinh.setSelectedItem("Nam");
        } else if (kh.getGioiTinh().equals("Nữ")) {
            txt_gioiTinh.setSelectedItem("Nữ");
        } else {
            txt_gioiTinh.setSelectedItem("Khác");
        }
        if (kh.getNhom() == 1) {
            txt_nhom.setSelectedItem("Thân thiết");
        } else {
            txt_nhom.setSelectedItem("Mới");
        }
        if (kh.getTrangThai() == 1) {
            txt_trangThai.setSelectedItem("Active");
        } else {
            txt_trangThai.setSelectedItem("Deactive");
        }
    }//GEN-LAST:event_tbl_khachHangMouseClicked

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        Integer confirm = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa khách hàng " + kh.getMaKhachHang() + " không?", "Xóa khách hàng.", 2);
        if (confirm == JOptionPane.YES_OPTION) {
            if (listKH.remove(kh) == true) {
                saveFile();
                getData(listKH);
                JOptionPane.showMessageDialog(this, "Xóa thành công!");
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại!");
            }
        }
    }//GEN-LAST:event_btn_xoaActionPerformed


